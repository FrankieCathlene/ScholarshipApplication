   private Connection getConnection() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            return DriverManager.getConnection("jdbc:mysql://localhost:3306/scholarship_app", "root", "");
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Database connection failed: " + e.getMessage());
            return null;
        }
    }

    private void loadScholarships() {
    try (Connection con = getConnection()) {
        if (con == null) return;

        String sql = "SELECT DISTINCT scholarship_type FROM students_list";
        PreparedStatement stmt = con.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        jComboBox1.removeAllItems(); // clear existing items

        while (rs.next()) {
            String scholarship = rs.getString("scholarship_type");
            jComboBox1.addItem(scholarship);
        }

        rs.close();
        stmt.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading scholarships: " + e.getMessage());
    }
}

   
  private void loadAcceptedStudentsForSelectedScholarship() {
    String selectedScholarship = (String) jComboBox1.getSelectedItem();
    if (selectedScholarship == null || selectedScholarship.trim().isEmpty()) {
        System.out.println("No scholarship selected.");
        return;
    }

    try (Connection con = getConnection()) {
        if (con == null) {
            System.out.println("Database connection failed.");
            return;
        }

        DefaultListModel<String> model = new DefaultListModel<>();

        String sql = "SELECT first_name, last_name FROM students_list " +
                     "WHERE scholarship_type = ? AND status = 'accepted'";
        PreparedStatement stmt = con.prepareStatement(sql);
        stmt.setString(1, selectedScholarship.trim());

        ResultSet rs = stmt.executeQuery();
        boolean hasResults = false;

        while (rs.next()) {
            String name = rs.getString("first_name") + " " + rs.getString("last_name");
            model.addElement(name);
            hasResults = true;
        }

        if (!hasResults) {
            model.addElement("No accepted students for this scholarship.");
        }

        jList1.setModel(model);

        rs.close();
        stmt.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading accepted students: " + e.getMessage());
    }
}


    

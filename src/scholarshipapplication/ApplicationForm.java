    private void btnSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave1ActionPerformed
                  
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/scholarship_app", "root", "");
        
        String sql = "INSERT INTO students_list (first_name, last_name, age, email, contact, address, status, low_income_proof, copy_of_id, copy_of_card, year_level, `Mother Name`, `Father Name`, Mobile_No, Guardian, Guardian_Address, Contact_No, parents_income, member_of_4ps, is_ips, scholarship_type, birth_date) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        
        PreparedStatement pstmt = conn.prepareStatement(sql);
        
        pstmt.setString(1, txtFName.getText());
        pstmt.setString(2, txtLName.getText());
        pstmt.setInt(3, Integer.parseInt(txtAge.getText()));
        pstmt.setString(4, txtEmail.getText());
        pstmt.setString(5, txtContact.getText());
        pstmt.setString(6, txtAddress.getText());
        pstmt.setString(7, "Pending");  // fixed status
        pstmt.setString(8, txtProofpath.getText());
        pstmt.setString(9, txtIdpath.getText());
        pstmt.setString(10, txtCardpath.getText());
        pstmt.setString(11, cmbYearLevel.getSelectedItem().toString());
        pstmt.setString(12, txtMotherName.getText());
        pstmt.setString(13, txtFatherName.getText());
        pstmt.setString(14, txtMobileNo.getText());
        pstmt.setString(15, txtGuardian.getText());
        pstmt.setString(16, txtGuardianAddress.getText());
        pstmt.setString(17, txtGuardianContact.getText());
        pstmt.setDouble(18, Double.parseDouble(txtParentIncome.getText()));
        pstmt.setString(19, chk4PsMember.isSelected() ? "Yes" : "No");
        pstmt.setString(20, chkIPSMember.isSelected() ? "Yes" : "No");
        pstmt.setString(21, cmbScholarshipType.getSelectedItem().toString());

        // Compose birthdate from combo boxes, e.g. "YYYY-MM-DD"
        String birthdate = comboYear.getSelectedItem() + "-" + comboMonth.getSelectedItem() + "-" + comboDate.getSelectedItem();
        pstmt.setString(22, birthdate);
       
        
        int rows = pstmt.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Data saved successfully!");
            dispose(); // Close current window
new Login().setVisible(true);
        }
        pstmt.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error saving data: " + e.getMessage());
    }
    // ======= END OF VALIDATION =======


    }//GEN-LAST:event_btnSave1ActionPerformed


        private void initBirthDateComboBoxes() {
    comboYear.removeAllItems();
    comboMonth.removeAllItems();
    comboDate.removeAllItems();

    // Add years (e.g. 1980-2025)
    for (int y = 1980; y <= 2025; y++) {
        comboYear.addItem(String.valueOf(y));
    }
    // Add months 1-12 (formatted with leading zero)
    for (int m = 1; m <= 12; m++) {
        comboMonth.addItem(String.format("%02d", m));
    }
    // Add days 1-31 (formatted with leading zero)
    for (int d = 1; d <= 31; d++) {
        comboDate.addItem(String.format("%02d", d));
    }
}

       

